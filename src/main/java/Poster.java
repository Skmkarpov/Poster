public class Poster {

    private final String poster;
    private final String name;
    private final String genre;
    private final boolean isTomorrow;

    public Poster(String poster, String name, String genre, boolean isTomorrow) {
        this.poster = poster;
        this.name = name;
        this.genre = genre;
        this.isTomorrow = isTomorrow;
    }

}